    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
            String indice = txtIndice.getText(); //para que me traiga el texto guardado
            int indi = Integer.parseInt(indice); //para transformar string en un entero
            if(indi<9){
                indi++;
                indice = String.valueOf(indi); //para volver a convertirlo a string
                txtIndice.setText(indice); //para cuando yo apriete el boton de adelante inciremente el indice
                
                textDni.setText(dni[indi]);
                txtNombre.setText(nombre[indi]);
                txtApellido.setText(apellido[indi]);
                txtDireccion.setText(direccion[indi]);
                txtTelefono.setText(telefono[indi]);
                txtFechaNac.setText(fecha_nac[indi]);
                
            }
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
            String indice = txtIndice.getText(); //para que me traiga el texto guardado
            int indi = Integer.parseInt(indice); //para transformar string en un entero
            if(indi>0){
                    indi--;
                    indice = String.valueOf(indi); //para volver a convertirlo a string
                    txtIndice.setText(indice); //para cuando yo apriete el boton de adelante inciremente el indice
                    
                    textDni.setText(dni[indi]);
                    txtNombre.setText(nombre[indi]);
                    txtApellido.setText(apellido[indi]);
                    txtDireccion.setText(direccion[indi]);
                    txtTelefono.setText(telefono[indi]);
                    txtFechaNac.setText(fecha_nac[indi]);
            }
    }//GEN-LAST:event_btnBackActionPerformed

   

